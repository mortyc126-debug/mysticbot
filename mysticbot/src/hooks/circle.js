export function getDailyCircleMood(element) {
  if (!element) return null;
  const moods = CIRCLE_MOODS[element] || CIRCLE_MOODS.fire;
  const dayIndex = Math.floor(Date.now() / (24 * 60 * 60 * 1000)) % moods.length;
  return moods[dayIndex];
}
