/**
 * Возвращает элемент по знаку Зодиака.
 * @param {string} sunSign — русское название знака
 * @returns {"fire"|"earth"|"air"|"water"|null}
 */
export function getElement(sunSign) {
  return ELEMENTS[sunSign] || null;
}

/**
 * Возвращает метаданные круга для знака пользователя.
 * Если знак неизвестен — возвращает null.
 */
export function getCircleMeta(sunSign) {
  const element = getElement(sunSign);
  return element ? ELEMENT_META[element] : null;
}

/**
 * Знаки, совместимые с данным кругом (та же стихия).
 */
export function getCircleSigns(element) {
  return ELEMENT_META[element]?.signs || [];
}

