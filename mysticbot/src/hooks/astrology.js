export const getSpecialDay = () => {
  const todayStr = new Date().toISOString().slice(0, 10);

  // Приоритет типов событий (меньше = важнее)
  const priority = { eclipse: 0, solstice: 1, equinox: 1, full_moon: 2, celtic: 2, portal: 3, new_moon: 4, solar: 5 };

  const todayEvents = MYSTICAL_CALENDAR_2026.filter(e => e.date === todayStr);

  if (todayEvents.length === 0) {
    const retro = isMercuryRetrograde();
    if (retro) {
      return {
        type: "mercury_retrograde",
        label: "☿ Меркурий ретроградный",
        description: `Меркурий ретроградный в знаке ${retro.sign}. Осторожно с контрактами, переговорами и новыми проектами. Время для переосмысления, возврата к незавершённому и внутреннего анализа.`,
        ritual: "Перечитай старые планы. Не подписывай важных договоров.",
        intensity: "medium",
        emoji: "☿",
        color: "rgba(139,92,246,0.12)",
        border: "rgba(139,92,246,0.3)",
        available_until: "23:59",
      };
    }
    return null;
  }

  const best = todayEvents.sort((a, b) => (priority[a.type] ?? 99) - (priority[b.type] ?? 99))[0];

  // Показывать только значимые события (не просто смену солнца)
  if (best.type === "solar") return null;

  const styleMap = {
    eclipse:   { color: "rgba(239,68,68,0.12)",    border: "rgba(239,68,68,0.4)",    intensity: "extreme" },
    solstice:  { color: "rgba(245,158,11,0.12)",   border: "rgba(245,158,11,0.4)",   intensity: "high" },
    equinox:   { color: "rgba(34,197,94,0.12)",    border: "rgba(34,197,94,0.4)",    intensity: "high" },
    full_moon: { color: "rgba(245,158,11,0.1)",    border: "rgba(245,158,11,0.3)",   intensity: "high" },
    celtic:    { color: "rgba(245,158,11,0.1)",    border: "rgba(245,158,11,0.3)",   intensity: "high" },
    portal:    { color: "rgba(139,92,246,0.15)",   border: "rgba(139,92,246,0.4)",   intensity: "high" },
    new_moon:  { color: "rgba(99,102,241,0.1)",    border: "rgba(99,102,241,0.3)",   intensity: "medium" },
  };

  const style = styleMap[best.type] || { color: "rgba(139,92,246,0.1)", border: "rgba(139,92,246,0.3)", intensity: "medium" };

  return {
    ...best,
    ...style,
    emoji: best.label.split(" ")[0],
    available_until: "23:59",
  };
};


// Получить события на конкретный месяц/год
export const getEventsForMonth = (year, month) => {
  return MYSTICAL_CALENDAR_2026.filter(e => {
    const d = new Date(e.date);
    return d.getFullYear() === year && d.getMonth() + 1 === month;
  });
};

// Получить ближайшие события (следующие N дней)
export const getUpcomingEvents = (days = 7) => {
  // Сравниваем по локальной дате (не UTC), чтобы события «сегодня»
  // не пропадали у пользователей в UTC+ зонах.
  const today = new Date();
  today.setHours(0, 0, 0, 0);
  const future = new Date(today.getTime() + days * 86400000);
  return MYSTICAL_CALENDAR_2026.filter(e => {
    const d = new Date(e.date);
    return d >= today && d <= future;
  });
};
