  const timeHint = preferredTime
    ? `Знак придёт в твоё привычное время — ${preferredTime}.`
    : hour < 18
      ? "Окно наблюдения открыто до завтрашнего вечера."
      : "Самое сильное время — следующие 24 часа.";

