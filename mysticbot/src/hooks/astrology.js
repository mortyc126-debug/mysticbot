export const getMasteryLevel = (totalReadings) => {
  for (let i = MASTERY_LEVELS.length - 1; i >= 0; i--) {
    if (totalReadings >= MASTERY_LEVELS[i].min) {
      return {
        level: MASTERY_LEVELS[i].level,
        emoji: MASTERY_LEVELS[i].emoji,
        next: MASTERY_LEVELS[i].next,
        current: MASTERY_LEVELS[i].min,
        reward: MASTERY_LEVELS[i].reward,
      };
    }
  }
  return { level: "Новичок", emoji: "🌱", next: 10, current: 0, reward: 0 };
};

