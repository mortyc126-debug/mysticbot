// ── Компонент ─────────────────────────────────────────────────────────────────

export default function Quizzes({ state, showToast }) {
  const { user, setCurrentPage, goBack, addLuck, updateOracleMemory } = state;

  const [activeQuiz, setActiveQuiz]       = useState(null);
  const [questionIndex, setQuestionIndex] = useState(0);
  const [answers, setAnswers]             = useState([]);   // array of tag strings
  const [result, setResult]               = useState(null);
  const [saved, setSaved]                 = useState(false);
  const [selectedOption, setSelectedOption] = useState(null);

  const completedQuizzes = user.completed_quizzes || [];

  const startQuiz = (quiz) => {
    setActiveQuiz(quiz);
    setQuestionIndex(0);
    setAnswers([]);
    setResult(null);
    setSaved(false);
    setSelectedOption(null);
    TelegramSDK.haptic.impact("medium");
  };

  const handleSelectOption = (tag, idx) => {
    if (selectedOption !== null) return; // prevent double-tap
    setSelectedOption(idx);
    TelegramSDK.haptic.impact("light");

    setTimeout(() => {
      const newAnswers = [...answers, tag];
      if (questionIndex < activeQuiz.questions.length - 1) {
        setAnswers(newAnswers);
        setQuestionIndex(qi => qi + 1);
        setSelectedOption(null);
      } else {
        // Последний вопрос — считаем результат
        const scores = {};
        newAnswers.forEach(t => { scores[t] = (scores[t] || 0) + 1; });
        const calcResult = activeQuiz.calcResult(scores);
        setResult(calcResult);
        setAnswers(newAnswers);
        setSelectedOption(null);
        TelegramSDK.haptic.notification("success");
      }
    }, 350);
  };

  const handleSaveResult = useCallback(() => {
    if (!result || !activeQuiz || saved) return;
    const alreadyDone = completedQuizzes.includes(activeQuiz.id);

    if (!alreadyDone) {
      const memoryText = activeQuiz.buildMemory(result, answers);
      updateOracleMemory({ [`quiz_${activeQuiz.id}`]: memoryText });
      addLuck(activeQuiz.luck, `Опросник «${activeQuiz.title}»`);
      state.completeQuiz?.(activeQuiz.id);
      showToast(`${activeQuiz.emoji} +${activeQuiz.luck} 💫 Сохранено в память Оракула!`);
    } else {
      showToast("✅ Результат уже сохранён");
    }
    setSaved(true);
    TelegramSDK.haptic.notification("success");
  }, [result, activeQuiz, answers, saved, completedQuizzes, addLuck, updateOracleMemory, showToast, state]);

  const handleBack = () => {
    if (result) {
      setResult(null);
      setAnswers([]);
      setQuestionIndex(0);
      setSelectedOption(null);
      setActiveQuiz(null);
    } else if (activeQuiz) {
      setActiveQuiz(null);
      setAnswers([]);
      setQuestionIndex(0);
      setSelectedOption(null);
    } else {
      goBack();
    }
  };

  const totalLuck = QUIZZES.reduce((sum, q) => sum + q.luck, 0);
  const earnedLuck = QUIZZES.filter(q => completedQuizzes.includes(q.id)).reduce((sum, q) => sum + q.luck, 0);

  // ── Экран результата ──────────────────────────────────────────────────────
  if (result && activeQuiz) {
    const isAlreadyDone = completedQuizzes.includes(activeQuiz.id);
    return (
      <div>
        <AppHeader title={`${activeQuiz.emoji} ${activeQuiz.title}`} luckPoints={user.luck_points} streak={user.streak_days} />
        <div style={{ padding: "14px 14px 100px", display: "flex", flexDirection: "column", gap: 14 }}>
          <button onClick={handleBack} style={{ background: "none", border: "none", color: "var(--text2)", fontSize: 13, cursor: "pointer", textAlign: "left", display: "flex", alignItems: "center", gap: 6, padding: 0 }}>
            ← Назад
          </button>

          {/* Результат */}
          <div style={{ textAlign: "center", padding: "24px 0 8px", animation: "pageEnter 0.4s ease" }}>
            <div style={{ fontSize: 72, marginBottom: 10, animation: "float 3s ease-in-out infinite" }}>
              {result.emoji}
            </div>
            <div style={{ fontSize: 22, fontWeight: 900, marginBottom: 8 }}>
              {result.name}
            </div>
            <div style={{
              display: "inline-block", fontSize: 11, fontWeight: 700,
              background: activeQuiz.color, border: `1px solid ${activeQuiz.border}`,
              borderRadius: 20, padding: "4px 14px", color: activeQuiz.accentColor, marginBottom: 16,
            }}>
              {activeQuiz.title}
            </div>
          </div>

          <Card style={{ background: activeQuiz.color, border: `1px solid ${activeQuiz.border}` }}>
            <p style={{ fontSize: 14, lineHeight: 1.7, color: "var(--text)", margin: 0 }}>
              {result.desc}
            </p>
            {result.growth && (
              <div style={{ marginTop: 12, paddingTop: 12, borderTop: `1px solid ${activeQuiz.border}` }}>
                <div style={{ fontSize: 11, color: "var(--text2)", marginBottom: 4 }}>Зона роста</div>
                <div style={{ fontSize: 13, fontWeight: 700, color: activeQuiz.accentColor }}>{result.growth}</div>
              </div>
            )}
          </Card>

          {/* Что сохраняется в Оракул */}
          <Card style={{ background: "rgba(139,92,246,0.06)", border: "1px solid rgba(139,92,246,0.2)" }}>
            <div style={{ fontSize: 12, color: "var(--text2)", marginBottom: 6 }}>🔮 Будет добавлено в память Оракула</div>
            <div style={{ fontSize: 12, color: "var(--text)", lineHeight: 1.6, fontStyle: "italic" }}>
              "{activeQuiz.buildMemory(result, answers)}"
            </div>
          </Card>

          {saved || isAlreadyDone ? (
            <div style={{
              textAlign: "center", padding: "14px",
              background: "rgba(34,197,94,0.1)", border: "1px solid rgba(34,197,94,0.3)",
              borderRadius: 14, fontSize: 13, color: "#4ade80", fontWeight: 700,
            }}>
              ✅ Результат сохранён в память Оракула
            </div>
          ) : (
            <Btn variant="primary" onClick={handleSaveResult} style={{ width: "100%" }}>
              🔮 Сохранить в Оракул · +{activeQuiz.luck} 💫
            </Btn>
          )}
        </div>
      </div>
    );
  }

  // ── Экран прохождения опросника ───────────────────────────────────────────
  if (activeQuiz) {
    const current = activeQuiz.questions[questionIndex];
    const progress = ((questionIndex) / activeQuiz.questions.length) * 100;

    return (
      <div>
        <AppHeader title={`${activeQuiz.emoji} ${activeQuiz.title}`} luckPoints={user.luck_points} streak={user.streak_days} />
        <div style={{ padding: "14px 14px 100px", display: "flex", flexDirection: "column", gap: 16 }}>
          <button onClick={handleBack} style={{ background: "none", border: "none", color: "var(--text2)", fontSize: 13, cursor: "pointer", textAlign: "left", display: "flex", alignItems: "center", gap: 6, padding: 0 }}>
            ← Отмена
          </button>

          {/* Прогресс */}
          <div>
            <div style={{ display: "flex", justifyContent: "space-between", marginBottom: 6 }}>
              <span style={{ fontSize: 11, color: "var(--text2)" }}>
                Вопрос {questionIndex + 1} из {activeQuiz.questions.length}
              </span>
              <span style={{ fontSize: 11, color: activeQuiz.accentColor, fontWeight: 700 }}>
                +{activeQuiz.luck} 💫 за прохождение
              </span>
            </div>
            <div style={{ background: "var(--border)", borderRadius: 10, height: 4, overflow: "hidden" }}>
              <div style={{
                height: "100%", borderRadius: 10,
                background: `linear-gradient(90deg, ${activeQuiz.accentColor}, #f59e0b)`,
                width: `${progress}%`, transition: "width 0.4s ease",
              }} />
            </div>
          </div>

          {/* Вопрос */}
          <div key={questionIndex} style={{ animation: "pageEnter 0.3s ease" }}>
            <h3 style={{ fontSize: 18, fontWeight: 800, lineHeight: 1.4, marginBottom: 20, textAlign: "center" }}>
              {current.q}
            </h3>

            <div style={{ display: "flex", flexDirection: "column", gap: 10 }}>
              {current.options.map((opt, idx) => (
                <div
                  key={idx}
                  onClick={() => handleSelectOption(opt.tag, idx)}
                  style={{
                    padding: "14px 16px", borderRadius: 14, cursor: "pointer",
                    border: `1.5px solid ${selectedOption === idx ? activeQuiz.accentColor : "var(--border)"}`,
                    background: selectedOption === idx ? activeQuiz.color : "var(--bg3)",
                    transition: "all 0.2s ease",
                    fontSize: 14, lineHeight: 1.4, color: "var(--text)",
                    opacity: selectedOption !== null && selectedOption !== idx ? 0.5 : 1,
                    transform: selectedOption === idx ? "scale(1.01)" : "scale(1)",
                  }}
                >
                  {opt.text}
                </div>
              ))}
            </div>
          </div>
        </div>
      </div>
    );
  }

  // ── Список опросников ─────────────────────────────────────────────────────
  return (
    <div>
      <AppHeader title="📋 Опросники" luckPoints={user.luck_points} streak={user.streak_days} />
      <div style={{ padding: "14px 14px 100px", display: "flex", flexDirection: "column", gap: 14 }}>
        <button onClick={goBack} style={{ background: "none", border: "none", color: "var(--text2)", fontSize: 13, cursor: "pointer", textAlign: "left", display: "flex", alignItems: "center", gap: 6, padding: 0 }}>
          ← Назад
        </button>

        {/* Заголовок */}
        <div style={{ textAlign: "center", padding: "8px 0" }}>
          <div style={{ fontSize: 48, marginBottom: 10 }}>🧬</div>
          <h2 style={{ fontSize: 18, fontWeight: 800, marginBottom: 6 }}>Раскрой себя</h2>
          <p style={{ fontSize: 13, color: "var(--text2)", lineHeight: 1.6 }}>
            Эти тесты помогают Оракулу лучше понять тебя и давать точные, персональные ответы.
            Каждый пройденный тест начисляет очки удачи.
          </p>
        </div>

        {/* Прогресс */}
        <Card style={{ background: "rgba(139,92,246,0.06)", border: "1px solid rgba(139,92,246,0.2)" }}>
          <div style={{ display: "flex", justifyContent: "space-between", alignItems: "center", marginBottom: 8 }}>
            <span style={{ fontSize: 13, fontWeight: 700 }}>Пройдено</span>
            <span style={{ fontSize: 13, color: "var(--accent)", fontWeight: 800 }}>
              {completedQuizzes.length}/{QUIZZES.length}
            </span>
          </div>
          <div style={{ background: "var(--border)", borderRadius: 10, height: 6, overflow: "hidden", marginBottom: 8 }}>
            <div style={{
              height: "100%", borderRadius: 10,
              background: "linear-gradient(90deg,#8b5cf6,#f59e0b)",
              width: `${(completedQuizzes.length / QUIZZES.length) * 100}%`,
              transition: "width 0.5s ease",
            }} />
          </div>
          <div style={{ fontSize: 11, color: "var(--text2)" }}>
            Собрано {earnedLuck}/{totalLuck} 💫 · осталось {totalLuck - earnedLuck} очков удачи
          </div>
        </Card>

        {/* Список */}
        <SLabel>Опросники</SLabel>
        <div style={{ display: "flex", flexDirection: "column", gap: 10 }}>
          {QUIZZES.map((quiz) => {
            const done = completedQuizzes.includes(quiz.id);
            return (
              <div
                key={quiz.id}
                onClick={() => startQuiz(quiz)}
                style={{
                  display: "flex", alignItems: "center", gap: 14,
                  background: done ? "rgba(34,197,94,0.05)" : "var(--bg2)",
                  border: `1px solid ${done ? "rgba(34,197,94,0.25)" : quiz.border}`,
                  borderRadius: 16, padding: "14px 16px", cursor: "pointer",
                  transition: "all 0.2s",
                }}
              >
                <div style={{
                  width: 48, height: 48, borderRadius: "50%", flexShrink: 0,
                  background: quiz.color, border: `1px solid ${quiz.border}`,
                  display: "flex", alignItems: "center", justifyContent: "center",
                  fontSize: 24,
                }}>
                  {quiz.emoji}
                </div>
                <div style={{ flex: 1, minWidth: 0 }}>
                  <div style={{ display: "flex", alignItems: "center", gap: 6, marginBottom: 2 }}>
                    <span style={{ fontSize: 14, fontWeight: 800 }}>{quiz.title}</span>
                    {done && <span style={{ fontSize: 10, color: "#4ade80", fontWeight: 700 }}>✓ ПРОЙДЕН</span>}
                  </div>
                  <div style={{ fontSize: 12, color: "var(--text2)", marginBottom: 4 }}>{quiz.desc}</div>
                  <div style={{ fontSize: 11, color: done ? "#4ade80" : quiz.accentColor, fontWeight: 700 }}>
                    {done ? "✅ Очки получены" : `+${quiz.luck} 💫 за прохождение`}
                  </div>
                </div>
                <div style={{ fontSize: 18, color: "var(--text2)", flexShrink: 0 }}>
                  {done ? "↩" : "→"}
                </div>
              </div>
            );
          })}
        </div>

        {completedQuizzes.length === QUIZZES.length && (
          <Card style={{ background: "rgba(245,158,11,0.08)", border: "1px solid rgba(245,158,11,0.3)", textAlign: "center" }}>
            <div style={{ fontSize: 36, marginBottom: 8 }}>🏆</div>
            <div style={{ fontSize: 14, fontWeight: 800, marginBottom: 4 }}>Все опросники пройдены!</div>
            <div style={{ fontSize: 12, color: "var(--text2)", lineHeight: 1.6 }}>
              Оракул знает тебя достаточно глубоко, чтобы давать максимально точные ответы.
            </div>
          </Card>
        )}
      </div>
    </div>
  );
}
